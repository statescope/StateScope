"""Curated example problems for the StateScope demo.

Every example is a real DriftMath :class:`Problem` (so it carries a SymPy-verified gold
trace -- the ground truth the demo compares a model against). No dataset, no labels, no
training: these are generated by the families and the CAS oracle supplies the gold state.

Each entry is deliberately a *trap* -- a place multi-step solvers tend to lose the thread
(extraneous roots, dropped domains, off-by-one recurrences, stale bindings, reused lemmas).

Because every entry is generator-backed, it can also be **reinstantiated with fresh
parameters** on demand (:func:`fresh_variant`) -- the demo's live proof that evaluation
instances are dynamic, not a static test file. Variants are session-scoped and addressed
as ``<base_id>#<seed>``.
"""

