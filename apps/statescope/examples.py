from driftmath.io.schema import Problem

# Pre-generate family instances we will hand-pick from (deterministic seeds).
_A = FamilyA().generate(8, seed=3)
_B = FamilyB().generate(8, seed=0)
_C = FamilyC().generate(9, seed=5)
_D = FamilyD().generate(6, seed=1)

_FAMILY_GENS = {"a": FamilyA, "b": FamilyB, "c": FamilyC, "d": FamilyD}
_REGEN_BATCH = 12  # instances per regeneration attempt (raises the template hit rate)


def _first(problems: list[Problem], **meta) -> Problem:
    for p in problems:
        if all(p.meta.get(k) == v for k, v in meta.items()):
            return p
    raise KeyError(meta)


