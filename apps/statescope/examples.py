EXAMPLES: dict[str, dict] = {e["id"]: e for e in _CURATED}

# Session-scoped fresh reinstantiations, addressed as "<base_id>#<seed>".
_VARIANTS: OrderedDict[str, Problem] = OrderedDict()
_MAX_VARIANTS = 64


def _entry(e: dict, problem: Problem, *, id_: str, seed: int | None = None) -> dict:
    return {
        "id": id_,
        "title": e["title"],
        "difficulty": e["difficulty"],
        "blurb": e["blurb"],
        "family": problem.family,
        "problem_text": problem.problem_text,
        "n_steps": len(problem.gold_trace.steps),
        "seed": seed,
    }


def list_examples() -> list[dict]:
    """Lightweight list for the UI (no heavy trace payloads)."""
    return [_entry(e, e["problem"], id_=e["id"]) for e in _CURATED]


def fresh_variant(example_id: str, seed: int | None = None) -> dict:
    """Reinstantiate a curated trap with fresh parameters; register and describe it.

    Uses the same family generator and template filter that produced the curated
    instance, trying a few seeds until the template appears in the batch.
    """
    base_id = example_id.split("#")[0]
    e = EXAMPLES.get(base_id)
    if e is None:
        raise KeyError(f"unknown example {base_id!r}")
    fam_key, match = e["regen"]
    s0 = seed if seed is not None else random.randrange(1, 100_000)
    for s in range(s0, s0 + 8):
        try:
            problem = _first(_FAMILY_GENS[fam_key]().generate(_REGEN_BATCH, seed=s), **match)
        except KeyError:
            continue
        vid = f"{base_id}#{s}"
        _VARIANTS[vid] = problem
        while len(_VARIANTS) > _MAX_VARIANTS:
            _VARIANTS.popitem(last=False)
        return _entry(e, problem, id_=vid, seed=s)
    raise KeyError(f"could not regenerate {base_id!r} near seed {s0}")


def get_problem(example_id: str) -> Problem:
    if example_id in _VARIANTS:
        return _VARIANTS[example_id]
    return EXAMPLES[example_id]["problem"]
