"""Build the AACL StateScope DemoBench: the released evaluation set for the demo paper.

250 problems, balanced across the four DriftMath families (A/B/C/D get n/4 each,
remainder to the earlier families: 250 -> 63/63/62/62). All problems are synthetic,
generated by the family generators with CAS-verified gold traces -- no MathQA or raw
MATH text is included (contamination policy for headline demo evidence).

Alongside the main JSONL it writes:
  - ``<stem>.manifest.json``  -- seed, family counts, git SHA, versions, provenance
  - ``<stem>.curated.jsonl``  -- the small hand-picked UI subset (screenshot examples);
    kept separate so curated examples are never counted as paper evidence.

Usage:
    python scripts/build_aacl_demobench.py --n 250 --seed 2026 --out data/aacl_statescope_demobench.jsonl
"""

from __future__ import annotations

import argparse
import datetime as _dt
import json
import subprocess
from collections import Counter
from pathlib import Path

import driftmath
from driftmath.families import registry
from driftmath.io.schema import DataRecord, Problem
from driftmath.io.storage import write_jsonl

DEFAULT_FAMILIES = ["family_a", "family_b", "family_c", "family_d"]


def split_counts(n: int, n_families: int) -> list[int]:
    """Balanced per-family counts; the remainder goes to the earliest families."""
    base, rem = divmod(n, n_families)
    return [base + (1 if i < rem else 0) for i in range(n_families)]


def generate_problems(n: int, seed: int, families: list[str]) -> list[Problem]:
    counts = split_counts(n, len(families))
    problems: list[Problem] = []
    for family, count in zip(families, counts):
        problems.extend(registry.get(family).generate(count, seed=seed))
    return problems


def build_records(problems: list[Problem]) -> list[DataRecord]:
    records = []
    for p in problems:
        rec = DataRecord.clean(p)
        rec.meta["dataset"] = "aacl_statescope_demobench"
        records.append(rec)
    return records


def curated_records() -> list[DataRecord]:
    """The StateScope UI's curated examples, flagged so they are never pooled with the 250."""
    import sys

    repo_root = str(Path(__file__).resolve().parents[1])
    if repo_root not in sys.path:  # `apps` is repo-local, not an installed package
        sys.path.insert(0, repo_root)
    from apps.statescope.examples import EXAMPLES  # lazy: pre-generates its problems on import

    records = []
    for ex_id, ex in EXAMPLES.items():
        rec = DataRecord.clean(ex["problem"])
        rec.meta.update({"dataset": "aacl_statescope_demobench_curated", "curated_ui": True, "ui_example_id": ex_id})
        records.append(rec)
    return records


def _git_sha() -> str | None:
    try:
        r = subprocess.run(["git", "rev-parse", "HEAD"], capture_output=True, text=True, timeout=5)
        return r.stdout.strip() or None if r.returncode == 0 else None
    except Exception:
        return None


def build(n: int, seed: int, out: Path, *, families: list[str] | None = None, include_curated: bool = True) -> dict:
    families = families or list(DEFAULT_FAMILIES)
    problems = generate_problems(n, seed, families)
    records = build_records(problems)
    write_jsonl(out, records)

    curated_path = None
    n_curated = 0
    if include_curated:
        curated = curated_records()
        curated_path = out.parent / f"{out.stem}.curated.jsonl"
        n_curated = write_jsonl(curated_path, curated)

    family_counts = Counter(r.family for r in records)
    manifest = {
        "timestamp": _dt.datetime.now(_dt.timezone.utc).isoformat(),
        "dataset": "aacl_statescope_demobench",
        "n": len(records),
        "seed": seed,
        "families": families,
        "family_counts": dict(sorted(family_counts.items())),
        "provenance_counts": dict(Counter(r.provenance for r in records)),
        "provenance_policy": (
            "problems come from the DriftMath family generators with CAS-verified gold traces: "
            "pure synthetic, or MATH-form-seeded templates re-instantiated with fresh parameters "
            "(template_reinstantiation); no raw MathQA/MATH text (contamination policy)"
        ),
        "generation": {"generator": "driftmath.families", "per_family_seed": seed, "conditions": ["clean"]},
        "git_sha": _git_sha(),
        "package_version": driftmath.__version__,
        "out": str(out),
        "curated_ui_subset": {
            "path": str(curated_path) if curated_path else None,
            "n": n_curated,
            "note": "hand-picked screenshot examples; NOT part of the 250-problem evidence set",
        },
    }
    manifest_path = out.parent / f"{out.stem}.manifest.json"
    manifest_path.write_text(json.dumps(manifest, indent=2), encoding="utf-8")
    return {"out": str(out), "manifest": str(manifest_path), "n": len(records), "family_counts": dict(family_counts)}


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(description="Build the AACL StateScope DemoBench (250 balanced problems).")
    ap.add_argument("--n", type=int, default=250)
    ap.add_argument("--seed", type=int, default=2026)
    ap.add_argument("--out", default="data/aacl_statescope_demobench.jsonl")
    ap.add_argument("--families", default=",".join(DEFAULT_FAMILIES), help="comma-separated family names")
    ap.add_argument("--no-curated", action="store_true", help="skip writing the curated UI subset")
    args = ap.parse_args(argv)

    families = [f.strip() for f in args.families.split(",") if f.strip()]
    result = build(args.n, args.seed, Path(args.out), families=families, include_curated=not args.no_curated)
    counts = ", ".join(f"{k}={v}" for k, v in sorted(result["family_counts"].items()))
    print(f"wrote {result['n']} problems ({counts}) -> {result['out']}")
    print(f"manifest -> {result['manifest']}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
